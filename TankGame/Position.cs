using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TankGame
{
	public class Position
	{
		public int x = 0;
		public int y = 0;

		public Position()
		{
			x = 0;
			y = 0;
		}

		public Position(int X, int Y)
		{
			x = X;
			y = Y;
		}
	}
}
